    <script>
        document.getElementById('loginForm').addEventListener('submit', async (e) => {
            e.preventDefault();

            const username = document.getElementById('username').value.trim();
            const password = document.getElementById('password').value;
            const alert = document.getElementById('alert');
            const loading = document.getElementById('loading');

            // Ocultar alert previo
            alert.classList.remove('show');
            loading.classList.add('show');

            try {
                const response = await fetch('backend/login.php', {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json'
                    },
                    body: JSON.stringify({ username, password })
                });

                const data = await response.json();
                loading.classList.remove('show');

                if (data.success) {
                    alert.className = 'alert success show';
                    alert.textContent = '‚úÖ ' + data.mensaje;

                    // Redirigir seg√∫n el rol
                    setTimeout(() => {
                        window.location.href = data.redirect;
                    }, 1000);
                } else {
                    alert.className = 'alert error show';
                    alert.textContent = '‚ùå ' + data.error;
                }
            } catch (error) {
                loading.classList.remove('show');
                alert.className = 'alert error show';
                alert.textContent = '‚ùå Error de conexi√≥n: ' + error.message;
            }
        });
    </script>
